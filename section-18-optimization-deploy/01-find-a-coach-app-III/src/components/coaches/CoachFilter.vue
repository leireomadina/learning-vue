<template>
  <base-card>
    <h2>Find Your Coach</h2>
    <form>
      <span class="filter-option">
        <input type="checkbox" name="frontend" id="frontend" checked @change="setFilter">
        <label for="frontend">Frontend</label>
      </span>
      <span class="filter-option">
        <input type="checkbox" name="backend" id="backend" checked @change="setFilter">
        <label for="frontend">Backend</label>
      </span>
      <span class="filter-option">
        <input type="checkbox" name="career" id="career" checked @change="setFilter">
        <label for="frontend">Career</label>
      </span>
    </form>
  </base-card>
</template>

<script>
export default {
  emits: ['change-filter'],
  data() {
    return {
      filters: {
        frontend: true,
        backend: true,
        career: true    
      }
    }
  },
  methods: {
    setFilter(event) {
      const inputId = event.target.id;
      const isActive = event.target.checked;
      const updatedFilters = {
        ...this.filters,
        [inputId]: isActive
      };
      // console.log(updatedFilters);
      // console.log('not updated filters', this.filters);
      this.filters = updatedFilters;
      // console.log('updated filters',this.filters);
      this.$emit('change-filter', updatedFilters);
    }
  }
}
</script>

